import UIKit

extension UIColor {
    class var darkRed: UIColor {
        return UIColor(hue: 0, saturation: 1, brightness: 0.3, alpha: 1)
    }
    
    class var darkGreen: UIColor {
        return UIColor(hue: 0.35, saturation: 1, brightness: 0.3, alpha: 1)
    }
    
    class var darkYellow: UIColor {
        return UIColor(hue: 0.2, saturation: 1, brightness: 0.3, alpha: 1)
    }
}
